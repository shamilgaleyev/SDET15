package HomeWorkLoops;

public class Task3 {
    public static void main(String[] args) {
        /*



Print numbers from 100 to 1

Print even numbers from 20 to 1 (2 ways)

Print odd numbers between 20 and 50 (2 ways)
         */
        int num = 100;
        while (num >= 1){
            System.out.println(num);
            num --;
        }
    }
}
