package HomeWorkLoops;

public class Task5 {
    public static void main(String[] args) {
        /*


Print odd numbers between 20 and 50 (2 ways)
         */
        int num = 20;
        while (num <= 50){
            if (num % 2 == 1){
                System.out.println(num);
            }
            num ++;
        }
    }
}
