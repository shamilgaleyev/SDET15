package class21;

public class Calculator {
    void add(int n1,int n2){
        System.out.println(n1+n2);
    }

    void add(int n1,int n2, int n3){
        System.out.println(n1+n2+n3);
    }

    void add(int n1,double n2){
        System.out.println(n1+n2);
    }

    void add(double n2,int n1){
        System.out.println(n1+n2);
    }

}
