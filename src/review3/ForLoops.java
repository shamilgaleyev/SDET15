package review3;

public class ForLoops {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i ++){
            System.out.println("Hello " + i);
        }
        System.out.println("+++++++++++++++++++++++");
        for (int a = 0; a < 8; a += 4){
            System.out.println("Bye");
        }
    }
}
